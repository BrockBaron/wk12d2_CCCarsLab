public class Tyre {

    private String tyreSize;
    private String tyreType;

    public Tyre(String tyreSize, String tyreType) {
        this.tyreSize = tyreSize;
        this.tyreType = tyreType;
    }

    public String getTyreSize() {
        return tyreSize;
    }

    public String getTyreType() {
        return tyreType;
    }
}
