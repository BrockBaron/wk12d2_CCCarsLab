public class Tyre {

    private int tyreSize;
    private String tyreType;

    public Tyre(int tyreSize, String tyreType) {
        this.tyreSize = tyreSize;
        this.tyreType = tyreType;
    }

    public int getTyreSize() {
        return tyreSize;
    }

    public String getTyreType() {
        return tyreType;
    }
}
